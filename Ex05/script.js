/*
Faça um programa JavaScript em que o usuário informará 3 números e o programa calculará a média aritmética desses números. */

let numero1 = prompt("Informe o primeiro número")
let numero2 = prompt("Informe o segundo número")
let numero3 = prompt("Informe o terceiro número")

let media = (numero1 + numero2 + numero3) /3
console.log(`A média dos 3 números informados é: ${media}`);
